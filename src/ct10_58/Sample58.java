package ct10_58;

/**
 * 「書籍」の抽象クラスと、インターフェース「借りる可能」を作成します。
 * 抽象クラス「書籍」には、書籍のタイトル、著者、ISBN番号を格納する属性と、書籍情報を出力する抽象メソッドを含めてください。
 * インターフェース「借りる可能」には、書籍を借りるときのメソッドと返却するときのメソッドを定義します。
 * 派生クラス「小説」、「学術書」を作成し、これらのクラスで「書籍」の抽象メソッドを実装し、「借りる可能」インターフェースのメソッドをオーバーライドしてください。
 * また、各書籍タイプに特有の属性やメソッドを追加してください。
 * 入力:
 * プログラムは特定の入力を必要としません。
 * 出力:
 * 各書籍の情報と、借りる/返却のプロセスを出力します。
 * 例:
 * 小説タイトル: ノルウェイの森, 著者: 村上春樹, ISBN: 123456789 小説を借りました。 学術書タイトル: 経済学の原理, 著者:
 * マンキュー, ISBN: 987654321 学術書を返却しました。
 */
public class Sample58 {

	public static void main(String[] args) {
		Novel novel = new Novel();
		novel.title = "ノルウェイの森";
		novel.author = "村上春樹";
		novel.isbnNumber = "123456789";
		novel.bookInfo();
		novel.borrow();
		Text text = new Text();
		text.title = "経済学の原理";
		text.author = "マンキュー";
		text.isbnNumber = "987654321";
		text.bookInfo();
		text.returning();
	}
	
	
}
